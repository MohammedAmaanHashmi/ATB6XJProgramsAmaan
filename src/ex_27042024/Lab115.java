package ex_27042024;

import java.sql.SQLOutput;

public class Lab115 {
    public static void main(String[] args) {
        //While
        //A - init
        //B - condition
        //C - Increment

        int i = 1; //A
        while (i <= 10) { //B
            //Execute the code
            System.out.println(i);
            i++;
        }

        for (int j = 1; j <= 10; j++) {
            System.out.println(j);
        }
    }
}
